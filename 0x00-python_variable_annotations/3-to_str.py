#!/usr/bin/env python3
""" Tells OS to execute it as python script"""
import math
""" imports math module """


def floor(n: float) -> int:
    """
    Computes the floor of the float n.

    Parameters:
    n (float): The float number.

    Returns:
    An integer which is the floor of n.
    """
    return math.floor(n)
